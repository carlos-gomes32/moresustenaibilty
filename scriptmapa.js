var btn = document.querySelector('#show-or-hide')
var div = document.querySelector ('.container-go')

btn.addEventListener ('click', function (){


if (div.style.display === 'block') {

div.style.display = 'none' ;

} else {

    div.style.display = 'block' ;
}


});



